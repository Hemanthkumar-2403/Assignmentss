
// ASSI : 10 BREAK IN LOOP

public class assi10 {
    public static void main(String arg[]){
        for (int i=1;i<=10;i++){
            if(i==6){
                System.out.println("Loop stopped at " +i);
                break;
            }
            System.out.print(i + " ");
        }
    }
    
}
